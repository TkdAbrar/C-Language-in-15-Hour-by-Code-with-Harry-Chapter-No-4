#include<stdio.h>

int main(){
    int i = 5;
    printf("The value of i++ is %d\n", ++i);
    i++; // First Print after increment
    ++i; // First increment after increment
    printf("The value of i is %d\n", i);
    
    i+=10; //==> Increment i by 10
    printf("The value of i is %d\n", i);
    return 0;





}